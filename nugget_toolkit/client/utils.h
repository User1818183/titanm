static const char *default_device(void) {
  struct stat statbuf;

  if (stat(DEV_CITADEL, &statbuf) == 0) {
    return DEV_CITADEL;
  }

  if (stat(DEV_DAUNTLESS, &statbuf) == 0) {
    return DEV_DAUNTLESS;
  }

  return 0;
}

